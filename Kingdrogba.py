import random  # Importing random module to randomly decide goalkeeper's dive


# Function for the penalty shootout
def penalty_shootout(goalkeeper, final):
    print(f"\n{final}: Didier Drogba vs {goalkeeper}!")  # Display match details
    print("Choose where to shoot:")
    print("1. Left\n2. Center\n3. Right")  # Options for user

    player_choice = input("Enter 1, 2, or 3: ")  # Taking user's choice

    # Checking if input is valid (1, 2, or 3)
    if player_choice == "1":
        player_direction = "Left"
    elif player_choice == "2":
        player_direction = "Center"
    elif player_choice == "3":
        player_direction = "Right"
    else:
        print("Invalid choice. Try again!")
        penalty_shootout(goalkeeper, final)  # Restart function if input is invalid
        return  # Exit function to prevent further execution

    keeper_direction = random.choice(["Left", "Center", "Right"])  # Randomly choosing GK dive direction

    print(f"\nDrogba shoots {player_direction}!")  # Display player shot direction
    print(f"{goalkeeper} dives {keeper_direction}!")  # Display GK dive direction

    # Checking if goalkeeper saves the shot
    if player_direction == keeper_direction:
        print(" SAVED! The goalkeeper stops the shot!")
    else:
        print(" GOAL! Drogba scores!")

    replay = input("Do you want to play again? (yes/no): ").lower()  # Asking user to replay
    if replay == "yes":
        main_game()  # Restart the game
    else:
        print("Thanks for playing!")  # End game if user says no


# Main function to start the game
def main_game():
    print("\nWelcome to Drogba's Penalty Shootout Challenge!")  # Welcome message
    print("Select a final to play:")  # Asking user to choose a final

    # Displaying available finals
    print("1. 2008 UCL Final vs Man United (Edwin van der Sar)")
    print("2. 2012 UCL Final vs Bayern Munich (Manuel Neuer)")
    print("3. 2022 EFL Cup Final vs Liverpool (Alisson Becker)")

    choice = input("Enter the number of the final you want to play: ")  # Taking user input

    # Checking user's selection and assigning goalkeeper and final name accordingly
    if choice == "1":
        penalty_shootout("Edwin van der Sar", "2008 UCL Final vs Man United")
    elif choice == "2":
        penalty_shootout("Manuel Neuer", "2012 UCL Final vs Bayern Munich")
    elif choice == "3":
        penalty_shootout("Alisson Becker", "2022 EFL Cup Final vs Liverpool")
    else:
        print("Invalid choice. Please restart the game.")  # If input is invalid, show error


# Start the game automatically
main_game()